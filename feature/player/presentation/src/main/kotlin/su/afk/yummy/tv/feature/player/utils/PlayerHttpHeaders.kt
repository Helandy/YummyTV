internal val BROWSER_STREAM_HEADERS: Map<String, String> = mapOf(
    USER_AGENT_HEADER to CHROME_UA,
)

internal fun Map<String, String>.withBrowserUserAgent(): Map<String, String> {
    val existingUserAgent = entries
        .firstOrNull { it.key.equals(USER_AGENT_HEADER, ignoreCase = true) }
        ?.value
        ?.takeIf { it.isNotBlank() }

    val withoutUserAgent = filterKeys { !it.equals(USER_AGENT_HEADER, ignoreCase = true) }
    return withoutUserAgent + (USER_AGENT_HEADER to (existingUserAgent ?: CHROME_UA))
}
